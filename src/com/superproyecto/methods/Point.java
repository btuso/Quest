package com.superproyecto.methods;

public class Point {

	private int mX;
	private int mY;

	public Point(int X, int Y) {
		this.mX = X;
		this.mY = Y;
	}

	public int getX() {
		return this.mX;
	}

	public int getY() {
		return this.mY;
	}

	public void setX(int X) {
		this.mX = X;
	}

	public void setY(int Y) {
		this.mY = Y;
	}
}
